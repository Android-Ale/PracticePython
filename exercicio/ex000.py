nome = input('Digite algo:')
print(type(nome))
print('É alfabético ?', nome.isalpha())
print('É número inteiro ?', nome.isnumeric())
print('É espaço ?', nome.isspace())
print('É número decimal ?', nome.isdecimal())
print('É letra maiuscula ?', nome.isupper())
print('É alfa númerico ?', nome.isalnum())
print('É letra minuscula ?', nome.islower())
print('É captalizada ?', nome.istitle())










